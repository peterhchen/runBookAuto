#!/usr/bin/python3

# Simple for loop
print ("simple forloop")
for i in [2, 4, 6, 8, 10]:
    print ("i = ", i)

# for loop by range
print ("forloop by range")
for i in range (10):
    print ("i = ", i)

# for loop by range (2, 10)
print ("forloop by range (2, 10)")
for i in range (2, 10):
    print ("i = ", i)
