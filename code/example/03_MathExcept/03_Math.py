#!/usr/bin/python3

# math function in Pyhton.

import math

# Access method in module.
print ('ceil (4.4) = ', math.ceil (4.4))
print ('floor (4.4) = ', math.floor (4.4))
print ('fabs (-4.4) = ', math.fabs (-4.4))

# factorial = 1 * 2 * 3 * 4
print ('factorial (4) = ', math.factorial (4))

# modulation
print ('fmod (5,4) = ', math.fmod (5, 4))

# modulation
print ('trunc (4, 2) = ', math.trunc (4.2))

# Power
print ('pow(2, 2) = ', math.pow (2, 2))

# Square root
print ('sqrt(4) = ', math.sqrt (4))

# Special Value
print ('math.e = ', math.e)
print ('math.pi = ', math.pi)

# e^x
print ('exp(4) = ', math.exp (4))

# log (20) = ln (20) => based on e
print ('log (2) = ', math.log (20))

# log (1000, 10) => based on 10
print ('log (1000, 10) = ', math.log (1000, 10))

# log10 (1000) => based on 10
print ('log10 (1000) = ', math.log10 (1000))

# convert radian to degree. Pi = 3.14159 => 180 degree
print ('degrees (3.14159) = ', math.degrees (3.14159))
# convert degree to radians. 180 => pi = 3.14159
print ('radians (180) = ', math.radians (180))
